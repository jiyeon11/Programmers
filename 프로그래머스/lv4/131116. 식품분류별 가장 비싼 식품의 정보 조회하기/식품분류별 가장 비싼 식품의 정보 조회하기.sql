SELECT CATEGORY,PRICE AS "MAX_PRICE",PRODUCT_NAME 
    FROM FOOD_PRODUCT
    WHERE (CATEGORY,PRICE) 
    IN(
        SELECT CATEGORY, MAX(PRICE)
        FROM FOOD_PRODUCT 
        GROUP BY CATEGORY
        ORDER BY PRICE DESC
    )
    AND CATEGORY IN ("식용유","과자","국","김치")
    GROUP BY CATEGORY
    ORDER BY PRICE DESC;